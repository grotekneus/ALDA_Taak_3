void GraphWidget::shuffle()
{
    const QList<QGraphicsItem *> items = scene()->items();
    for (QGraphicsItem *item : items) {
        if (qgraphicsitem_cast<Node *>(item))
            item->setPos(-150 + QRandomGenerator::global()->bounded(300), -150 + QRandomGenerator::global()->bounded(300));
    }
}

void GraphWidget::zoomIn()
{
    scaleView(qreal(1.2));
}

void GraphWidget::zoomOut()
{
    scaleView(1 / qreal(1.2));
}

void GraphWidget::createFromMatrix(std::vector<std::vector<int>> matrix,std::vector<char> infected)
{
    std::vector<Node*> nodes;
    for(int i = 0; i<matrix[0].size();i++){
        Node *node;
        char charValue = (char) i+65;
        bool containsInt = std::find(infected.begin(), infected.end(), charValue) != infected.end();
        if(containsInt){
            node = new Node(this,charValue,true);
        }
        else{
            node = new Node(this,charValue,false);
        }
        nodes.push_back(node);
        this->scene()->addItem(node);
    }
    for(int k = 0; k<matrix.size();k++){
        for(int l = 0; l<matrix.size();l++){
            if(matrix[l][k] == 1){
                this->scene()->addItem(new Edge(nodes[l], nodes[k]));
            }
        }
    }
}
